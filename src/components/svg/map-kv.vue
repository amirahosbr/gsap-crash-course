import { gsap } from "gsap";

onMounted(async () => {
	await nextTick();

	const highlightColor = "#FDD000";
	const defaultColor = "#F2F2F2";

	// Initial setup â€” hide all images & text, reset path fill
	gsap.set(
		[
			"#sapporo text",
