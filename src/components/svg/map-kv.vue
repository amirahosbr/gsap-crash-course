				},
				"-=0.5",
			)
			.to([`#${city} text`, `#${city} image`], {
				// Fade out text and image (0.5s)
				opacity: 0,
				duration: 0.5,
				ease: "power2.inOut",
				delay: 0.8,
			})
			.to(
				`#${city} path`,
				{
					// Reset path fill back to default (0.5s)
					fill: defaultColor,
					duration: 0.5,
					ease: "power2.inOut",
				},
				"-=0.5",
			);
	}
});
